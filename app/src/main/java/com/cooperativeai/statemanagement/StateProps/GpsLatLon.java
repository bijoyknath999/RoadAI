package com.cooperativeai.statemanagement.StateProps;

public class GpsLatLon {

    private double lat=0;
    private double lon=0;

    public GpsLatLon(double lat,double lon){
        this.lat = lat;
        this.lon = lon;
    }

    public double getLat() {
        return lat;
    }

    public double getLon() {
        return lon;
    }

}
